import '../../domain/entities/treatment_entity.dart';

class TreatmentModel extends TreatmentEntity {
  const TreatmentModel({
    required super.patientName,
    required super.patientPhone,
    required super.patientEmail,
    required super.gender,
    required super.doctorName,
    required super.clinicName,
    required super.appointmentId,
    required super.dateStart,
    required super.dateEnd,
    required super.invoiceId,
    required super.diagnosisDate,
    required super.fullReadyDiag,
    required super.diagnosisDescription,
    required super.diagDetails,
    required super.doctorNotes,
    required super.treatmentDescription,
    required super.drugName,
    required super.contraDesc,
    required super.contraList,
    required super.departmentName,
    required super.sideEffectDescription,
    required super.treatmentDuration,
    required super.administrationMethod,
    required super.storageConditions,
    required super.brandName,
    required super.typeName,
    required super.warnings,
    required super.drugInteractions,
    required super.dosageDescription,
    required super.chemicalName,
    required super.categoryName,
    required super.manufacturerName,
    required super.registrationNumber,
  });

  factory TreatmentModel.fromJson(Map<String, dynamic> json) {
    return TreatmentModel(
      patientName: json['patient_name'] ?? '',
      patientPhone: json['patient_phone'] ?? '',
      patientEmail: json['patient_email'] ?? '',
      gender: json['gender'] ?? '',
      doctorName: json['doctor_name'] ?? '',
      clinicName: json['clinic_name'] ?? '',
      appointmentId: json['appointmentid'] ?? '',
      dateStart: json['date_start'] ?? '',
      dateEnd: json['date_end'] ?? '',
      invoiceId: json['invoiceid'] ?? '',
      diagnosisDate: json['diagnosisdate'] ?? '',
      fullReadyDiag: json['full_ready_diag'] ?? '',
      diagnosisDescription: json['diagnosis_description'] ?? '',
      diagDetails: json['diag_details'] ?? '',
      doctorNotes: json['doctor_notes'] ?? '',
      treatmentDescription: json['treatment_description'] ?? '',
      drugName: json['drug_name'] ?? '',
      contraDesc: json['contra_desc'] ?? '',
      contraList: json['contra_list'] ?? '',
      departmentName: json['department_name'] ?? '',
      sideEffectDescription: json['side_effect_description'] ?? '',
      treatmentDuration: json['treatment_duration'] ?? '',
      administrationMethod: json['administration_method'] ?? '',
      storageConditions: json['storage_conditions'] ?? '',
      brandName: json['brand_name'] ?? '',
      typeName: json['type_name'] ?? '',
      warnings: json['warnings'] ?? '',
      drugInteractions: json['drug_interactions'] ?? '',
      dosageDescription: json['dosage_description'] ?? '',
      chemicalName: json['chemical_name'] ?? '',
      categoryName: json['category_name'] ?? '',
      manufacturerName: json['manufacturer_name'] ?? '',
      registrationNumber: json['registration_number'] ?? '',
    );
  }
}
